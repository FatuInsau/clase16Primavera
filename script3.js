let numeros = [5,5,5,8,41];

// push
// agrega elementos al final del array
// retorna el nuevo largo o la cant nueva de elementos

numeros.push("Estoy en el ultimo lugar",true)

// Unshift
// agrega elementos al principio del array
// retorna el nuevo largo o la cant nueva de elementos

numeros.unshift("Estoy al principio",false)


